#include <unistd.h>
#include <stdlib.h>

int main(int ac, char **av)
{
    int i = 0;

    if (ac > 1)
    {
        while (av[1][i])
        {
            if ((av[1][i] >= 65) && (av[1][i] <= 90))
            {
                av[1][i] = av[1][i] + 32;
                write(1, "_", 1);
            }
            write(1, &av[1][i], 1);
            i++;
        }
    }
    write(1, "\n", 1);
}