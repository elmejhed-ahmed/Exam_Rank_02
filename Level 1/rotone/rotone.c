#include <unistd.h>

int main(int ac, char **av)
{
    int i = 0;
    if(ac > 1)
    {
        while (av[1][i])
        {
            if((av[1][i] > 96 && av[1][i] < 122) || (av[1][i] > 64 && av[1][i] < 90))
                av[1][i] += 1;
            else if (av[1][i] == 'z' || av[1][i] == 'Z')
                av[1][i] -= 25;
            write(1, &av[1][i], 1);
            i++;
        }
    }
    write(1, "\n", 1);
}